#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*p;
	int		i;
	int		l;

	if (!s1)
		return (NULL);
	i = 0;
	l = 0;
	while (s1[l])
		l++;
	p = malloc(sizeof (char) * (l + 1));
	if (p == NULL)
		return (NULL);
	while (s1[i] != '\0')
	{
		p[i] = s1[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
