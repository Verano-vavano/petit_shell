#include "libft.h"

char	*ft_strndup(const char *s, size_t len)
{
	char	*dest;
	size_t	i;

	dest = ft_calloc(len + 1, sizeof (char));
	if (!dest)
		return (0);
	i = 0;
	while (i < len)
	{
		dest[i] = s[i];
		i++;
	}
	return (dest);
}
